# 3//2*1 + 3%2 = 2 -> 남는병 // 1 -> 받은병 
# 2//2*1 + 2%2 = 1 -> 남는병 // 1 -> 받은병 // 1>=2 종료

# n//a*b + n%a -> 남는 병
# n//a*b -> 받은 병

