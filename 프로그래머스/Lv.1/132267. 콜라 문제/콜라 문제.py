def solution(a, b, n):
    answer = 0
    while n >= a:
        answer += (n//a)*b
        n = (n//a)*b + n%a
    return answer

# a=2, b=1, n=20
# 20//2*1 + 20%2 = 10 -> 남는 병 // 10 -> 받은병 
